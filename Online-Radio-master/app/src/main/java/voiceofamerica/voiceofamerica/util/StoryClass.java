public class StoryClass {

    private String story_title;
    private String story_writter;
    private String story_url;

    public String getStory_title() {
        return story_title;
    }

    public void setStory_title(String story_title) {
        this.story_title = story_title;
    }

    public String getStory_writter() {
        return story_writter;
    }

    public void setStory_writter(String story_writter) {
        this.story_writter = story_writter;
    }

    public String getStory_url() {
        return story_url;
    }

    public void setStory_url(String story_url) {
        this.story_url = story_url;
    }
}
